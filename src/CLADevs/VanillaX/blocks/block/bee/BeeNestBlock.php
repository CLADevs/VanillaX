<?php

namespace CLADevs\VanillaX\blocks\block\bee;

use CLADevs\VanillaX\blocks\utils\BlockVanilla;
use CLADevs\VanillaX\items\ItemIdentifiers;
use pocketmine\block\Block;
use pocketmine\block\BlockBreakInfo;
use pocketmine\block\BlockIdentifier;
use pocketmine\block\BlockToolType;
use pocketmine\block\Opaque;
use pocketmine\item\Item;
use pocketmine\math\Vector3;
use pocketmine\player\Player;
use pocketmine\world\BlockTransaction;

class BeeNestBlock extends Opaque{

    private int $facing = 0;
    
    //TODO functionality
    public function __construct(){
        parent::__construct(new BlockIdentifier(BlockVanilla::BEE_NEST, 0, ItemIdentifiers::BEE_NEST), "Bee Nest", new BlockBreakInfo(0.3, BlockToolType::PICKAXE, 0, 0.3));
    }

    public function place(BlockTransaction $tx, Item $item, Block $blockReplace, Block $blockClicked, int $face, Vector3 $clickVector, ?Player $player = null): bool{
        if($player !== null){
            $facing = [
                4 => 3,
                3 => 2,
                2 => 0,
                5 => 1
            ];
            $this->facing = $facing[$player->getHorizontalFacing()];
        }
        return parent::place($tx, $item, $blockReplace, $blockClicked, $face, $clickVector, $player); // TODO: Change the autogenerated stub
    }

    protected function writeStateToMeta(): int{
        return $this->facing;
    }
}